public class logicaloperator {
    public static void main(String[] args) {
        //for  logical And
        System.out.println((3>2)&&(5>0));
        System.out.println((3<2)&&(5>0));

        //for logical Or
        System.out.println((3>2)||(5<2));
        System.out.println((3<2)||(5<2));

        //for logical not
        System.out.println(!(3>2));
        System.out.println(!(5<4));
    }
}
